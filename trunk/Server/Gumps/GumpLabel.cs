using System;
using Server.Network;

namespace Server.Gumps
{
    public class GumpLabel : GumpEntry
    {
        private int m_X, m_Y;
        private int m_Hue;
        private string m_Text;

        public GumpLabel(int x, int y, int hue, string text)
        {
            this.m_X = x;
            this.m_Y = y;
            this.m_Hue = hue;
            this.m_Text = text;
        }

        public int X
        {
            get
            {
                return this.m_X;
            }
            set
            {
                this.Delta(ref m_X, value);
            }
        }

        public int Y
        {
            get
            {
                return this.m_Y;
            }
            set
            {
                this.Delta(ref m_Y, value);
            }
        }

        public int Hue
        {
            get
            {
                return this.m_Hue;
            }
            set
            {
                this.Delta(ref m_Hue, value);
            }
        }

        public string Text
        {
            get
            {
                return this.m_Text;
            }
            set
            {
                this.Delta(ref m_Text, value);
            }
        }

        public override string Compile()
        {
            return String.Format("{{ text {0} {1} {2} {3} }}", this.m_X, this.m_Y, this.m_Hue, this.Parent.Intern(this.m_Text));
        }

        private static readonly byte[] m_LayoutName = Gump.StringToBuffer("text");

        public override void AppendTo(IGumpWriter disp)
        {
            disp.AppendLayout(m_LayoutName);
            disp.AppendLayout(this.m_X);
            disp.AppendLayout(this.m_Y);
            disp.AppendLayout(this.m_Hue);
            disp.AppendLayout(this.Parent.Intern(this.m_Text));
        }
    }
}